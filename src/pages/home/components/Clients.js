export default function Clients() {
  var settings = {
    dots: true,
    infinite: true,
    speed: 1000,
    slidesToShow: 1,
    autoplay: true,
    slidesToScroll: 1,
    nextArrow: <SampleNextArrow />,
    prevArrow: <SamplePrevArrow />,
  }
  const renderItem = pageNo => {
    var results = []
    for (let index = 1; index <= pageSize; index++) {
      const idx = pageNo * pageSize + index
      const img = images.find(item => item.id === idx)
      if (img) {
        results.push(
          <div className="CoCMynystk" key={idx}>
            <img alt="" className="NAKfdAoUxT" src={img.image} />
          </div>
        )
      }
    }
    return results
  }
  function render() {
    const pageNo = images.length / pageSize
    var results = []
    for (let index = 0; index < pageNo; index++) {
      let pageUI = (
        <div className="VgEAMkHmJM" key={index}>
          <div className="nTYFkWTGxl">{renderItem(index)}</div>
        </div>
      )
      results.push(pageUI)
    }
    return results
  }
  return (
    <>
      <div className="vlShojONFe">
        <ContainerMain>
          <div className="gUHTRgaYhN">Clients</div>
          <Slider className="PzCqhEeSrK" {...settings}>
            {render()}
          </Slider>
        </ContainerMain>
      </div>
    </>
  )
}
function SampleNextArrow(props) {
  const { className, style, onClick } = props
  return (
    <RightOutlined
      className={className}
      style={{ ...style, display: "block", color: "#777777", fontSize: 14 }}
      onClick={onClick}
    />
  )
}

function SamplePrevArrow(props) {
  const { className, style, onClick } = props
  return (
    <LeftOutlined
      className={className}
      style={{ ...style, display: "block", color: "#777777", fontSize: 14 }}
      onClick={onClick}
    />
  )
}
